    public void loadDV() {
        File f = new File(fDV);
        if (f.exists()) {
            listDV = IOFile.doc(fDV);
        } else {
            listDV = new ArrayList<>();

        }
    }

    public void loadNV() {
        File f = new File(fNV);
        if (f.exists()) {
            listNV = IOFile.doc(fNV);
        } else {
            listNV = new ArrayList<>();
        }
    }

    private NhanVien getNVByMa(int ma) {
        for (int i = 0; i < tmNV.getRowCount(); i++) {
            if (Integer.parseInt(tmNV.getValueAt(i, 0).toString()) == ma) {
                return new NhanVien(ma,
                        tmNV.getValueAt(i, 1).toString(),
                        tmNV.getValueAt(i, 2).toString(),
                        tmNV.getValueAt(i, 3).toString());
            }
        }
        return null;
    }

    private DichVu getDVByMa(int ma) {
        for (int i = 0; i < tmDV.getRowCount(); i++) {
            if (Integer.parseInt(tmDV.getValueAt(i, 0).toString()) == ma) {
                return new DichVu(ma,
                        tmDV.getValueAt(i, 1).toString(),
                        tmDV.getValueAt(i, 2).toString(),
                        Double.parseDouble(tmDV.getValueAt(i, 3).toString()),
                        Double.parseDouble(tmDV.getValueAt(i, 4).toString()));
            }
        }
        return null;
    }

    public void loadQL() {
        File f = new File(fQL);
        if (f.exists()) {
            listQL = IOFile.doc(fQL);
        } else {
            listQL = new ArrayList<>();
        }
    }

    public void showDV(List<DichVu> list) {
        tmDV.setRowCount(0);
        for (DichVu i : list) {
            tmDV.addRow(i.toObject());
        }
    }

    public void showQL(List<QLBH> list) {
        tmQL.setRowCount(0);
        for (QLBH i : list) {
            int maNV = i.getMaNV();
            String tenNV = getNVByMa(maNV).getTen();
            int maDV = i.getMaDV();
            String tenDV = getDVByMa(maDV).getTen();
            int sl = i.getSoLuong();
            tmQL.addRow(new Object[]{maNV, tenNV, maDV, tenDV, sl});
        }
    }

    public void showNV(List<NhanVien> list) {
        tmNV.setRowCount(0);
        for (NhanVien i : list) {
            tmNV.addRow(i.toObject());
        }
    }

